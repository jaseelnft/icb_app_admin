export const showToast = (msg: string) => toast(msg, "succes");

export const showErrorToast = (msg: string) => toast(msg, "error");

export const showWarningToast = (msg: string) => toast(msg, "warning");

let cssAdded = false;

const toast = (msg: string, err: string) => {
  if (!cssAdded) addcss();

  const div = document.createElement("div");
  div.id = "appToast";
  div.innerHTML = msg;
  if (err === "error") div.className = "red";
  if (err === "warning") div.className = "orange";
  document.body.appendChild(div);
  setTimeout(() => {
    const element = document.getElementById("appToast");
    if (element) element.remove();
  }, 4000);
};

